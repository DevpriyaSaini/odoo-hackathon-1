const adminNavItems = [
  { href: '/dashboard/admin', label: 'Dashboard', icon: DashboardIcon },
  { href: '/employees', label: 'Employees', icon: UsersIcon },
  { href: '/attendance', label: 'Attendance', icon: ClockIcon },
  { href: '/leaves', label: 'Leave Requests', icon: CalendarIcon },
  { href: '/payroll', label: 'Payroll', icon: WalletIcon },
];

const employeeNavItems = [
  { href: '/dashboard/employee', label: 'Dashboard', icon: DashboardIcon },
  { href: '/profile', label: 'My Profile', icon: UserIcon },
  { href: '/attendance', label: 'Attendance', icon: ClockIcon },
  { href: '/leaves', label: 'Leave', icon: CalendarIcon },
  { href: '/payroll', label: 'Payroll', icon: WalletIcon },
];

export default function Sidebar() {
  const pathname = usePathname();
  const { user, logout, isAdmin } = useAuth();

  const navItems = isAdmin() ? adminNavItems : employeeNavItems;

  const getUserInitials = () => {
    if (!user?.email) return 'U';
    return user.email.charAt(0).toUpperCase();
  };

  return (
    <aside className="sidebar">
      {/* Logo */}
      <div className="sidebar-logo">
        <h1>Day<span>flow</span></h1>
      </div>

      {/* Navigation */}
      <nav className="sidebar-nav">
        {navItems.map((item) => {
          const Icon = item.icon;
          const isActive = pathname === item.href || 
            (item.href !== '/dashboard/admin' && item.href !== '/dashboard/employee' && pathname.startsWith(item.href));
          
          return (
            <Link
              key={item.href}
              href={item.href}
              className={`sidebar-link ${isActive ? 'active' : ''}`}
            >
              <Icon />
              <span>{item.label}</span>
            </Link>
          );
        })}
      </nav>

      {/* Footer with user info */}
      <div className="sidebar-footer">
        <div className="sidebar-user">
          <div className="sidebar-user-avatar">
            {getUserInitials()}
          </div>
          <div className="sidebar-user-info">
            <div className="sidebar-user-name">{user?.email?.split('@')[0] || 'User'}</div>
            <div className="sidebar-user-role">{user?.role || 'Employee'}</div>
          </div>
        </div>
        <button 
          onClick={logout}
          className="sidebar-link"
          style={{ marginTop: '12px', width: '100%', border: 'none', background: 'transparent', cursor: 'pointer', textAlign: 'left' }}
        >
          <LogoutIcon />
          <span>Logout</span>
        </button>
      </div>
    </aside>
  );
}
